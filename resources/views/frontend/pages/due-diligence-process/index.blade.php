                                <h4>Financial due diligence</h4>
                                <p>Financial due diligence is a comprehensive and investigative process aiming to assess a company’s financial strengths by examining its financial data, which may include historical sales, expenses and working capital, as well as key assumptions used in management’s forecast. The aim of financial due diligence is to help investors see if the deal is worthwhile and have a better understanding of the potential risks and opportunities before signing on the dotted line.</p>
                                <p>Financial due diligence is typically carried out before mergers and acquisitions. Other circumstances that warrant financial due diligence may include when a company looks to get additional funding or when it is in dispute with another organisation.</p>
                                <h4>Legal due diligence</h4>
                                <p>Legal due diligence reviews legal risks that exist in a company, stemming from its organisation structure to contracts that it has with its suppliers, clients, partners and also employees. In addition, how the upcoming deal is structured may also have legal implications.</p>
                                <strong>Common areas that our legal due diligence team reviews include:</strong>
                                <ul class="listStyle mb-4">
                                    <li><i class="fas fa-arrow-circle-right"></i> Ownership of a company (share certificates issued to board members, directors or key stakeholders)</li>
                                    <li><i class="fas fa-arrow-circle-right"></i> Management structures</li>
                                    <li><i class="fas fa-arrow-circle-right"></i> Company procedures</li>
                                    <li><i class="fas fa-arrow-circle-right"></i> Legal obligations such as loans</li>
                                    <li><i class="fas fa-arrow-circle-right"></i> Ongoing litigation, if any</li>
                                </ul>
                                <h4>Human resource due diligence</h4>
                                <p>Human resource due diligence assesses the risks and opportunities pertaining to the employment structure of a company. It usually includes HR operations, employment contracts, grade and salary bands, leave policies, benefits, as well as any on-going disciplinary cases. The aim is to understand the company culture of a target company and see how it can facilitate a smooth transition.</p>
                                <h4>International due diligence</h4>
                                <p>This form of due diligence is vital to companies that already have operations in numerous countries and legal jurisdictions or are planning to go international. International due diligence is the process of acquiring and analysing local information not publicly available outside of the location. At Trademark Nova, we have helped companies to obtain:</p>
                                <ul class="listStyle mb-4">
