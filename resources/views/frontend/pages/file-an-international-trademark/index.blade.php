                        <div class="col-md-12">
                            <h4>Online Filing</h4>
                            <p>To file an international application based on your local trademark registration or application, you must submit it online through UKIPO and specify which countries you want your trademark to cover, with over 90 countries to choose from. After checking for compliance and matching with your local application, UKIPO forwards the application to WIPO. Due to Brexit, EU trademarks and international applications involving the EU are subject to measures to protect existing rights, including automatic conversion of registered trademarks and the ability to file UK applications within nine months after the transition period with the retained priority date. More information is available on the websites of EUIPO and UKIPO.</p>
                            <div class="callout-info">
                                <p class="m-0">If you file the international application within six months of applying for the UK trademark registration, you can claim priority for it. This provides the advantage of starting trademark protection on the same date in all countries covered by the application. You can claim priority for almost every country in the world that is a member of the Treaty of Paris or the World Trade Organization.</p>
                            </div>
                        </div>
