                            <div class="callout-info">
                                <p class="m-0">If you have a national trademark registration or an application in another country that is a member of the Madrid System, we can help you extend it via the relevant authority. The details in your national trademark registration and your international application must match. You're not required to apply for international trademark registration for all goods and services covered by your national registration, but you can only register the same or fewer goods and services.</p>
                            </div>
